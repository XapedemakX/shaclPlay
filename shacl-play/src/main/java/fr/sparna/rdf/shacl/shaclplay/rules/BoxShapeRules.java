package fr.sparna.rdf.shacl.shaclplay.rules;

public class BoxShapeRules {
	
	protected String ShSparqlRuleName;
	protected String RdfsLabel;
	protected String RdfsComments;
	protected String ShOrder;
	protected String ShPrefixes;
	protected String ShConstruct;
	
	 
	
	public String getShSparqlRuleName() {
		return ShSparqlRuleName;
	}
	public void setShSparqlRuleName(String shSparqlRuleName) {
		ShSparqlRuleName = shSparqlRuleName;
	}
	public String getRdfsLabel() {
		return RdfsLabel;
	}
	public void setRdfsLabel(String rdfsLabel) {
		RdfsLabel = rdfsLabel;
	}
	public String getRdfsComments() {
		return RdfsComments;
	}
	public void setRdfsComments(String rdfsComments) {
		RdfsComments = rdfsComments;
	}
	public String getShOrder() {
		return ShOrder;
	}
	public void setShOrder(String shOrder) {
		ShOrder = shOrder;
	}
	public String getShPrefixes() {
		return ShPrefixes;
	}
	public void setShPrefixes(String shPrefixes) {
		ShPrefixes = shPrefixes;
	}
	public String getShConstruct() {
		return ShConstruct;
	}
	public void setShConstruct(String shConstruct) {
		ShConstruct = shConstruct;
	}

}
