package fr.sparna.rdf.shacl.shacl2xsd;

public class OntologyImports {
	
	protected String ImportSchema;
	protected String ImportURI;
	
	public String getImportSchema() {
		return ImportSchema;
	}
	public void setImportSchema(String importSchema) {
		ImportSchema = importSchema;
	}
	public String getImportURI() {
		return ImportURI;
	}
	public void setImportURI(String importURI) {
		ImportURI = importURI;
	}
	
	

}
